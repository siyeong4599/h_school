CREATE DATABASE SCOTT;
USE SCOTT;

CREATE TABLE EMP(
	EMPNO	INT(4)	NOT NULL	PRIMARY KEY,		
	ENAME	VARCHAR(10),				
	JOB	VARCHAR(9),				
	MGR	INT(4) ,		
	HIREDATE	DATE,
	SAL	DOUBLE(7,2),
	COMM	DOUBLE(7,2),
	DEPTNO	INT(2),
	INDEX(MGR),
	INDEX(DEPTNO)
);

CREATE TABLE DEPT(
	DEPTNO INT(2) NOT NULL PRIMARY KEY,
	DNAME VARCHAR(20),
	LOC VARCHAR(13)
);

CREATE TABLE SALGRADE(
	GRADE INT(1),
	LOSAL INT(7),
	HISAL INT(7)
);


